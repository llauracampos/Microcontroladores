; E UM NOME COERENTE ÀS SUAS FUNÇÕES.

	
; PARA A CONTAGEM DO TIMER 2, FORAM DETERMINADOS:
;PRÉ-SCALER DE 1:16.
;POS-SCALER DE 1:16.
;PR2 DE 194.
	
; DESSA FORMA, O TIMER 2 REALIZARÁ A CONTAGEM MAIS PRÓXIMA POSSÍVEL DE 50000US, QUE É DE 49920US.

DELAY_50MS ; LABEL QUE REALIZARPA A CONTAGEM DO DELAY DE 50MS (50000US).
	BSF	T2CON, TMR2ON ; INICIA A CONTAGEM DO TIMER 2.
	CLRF	ESTOURO ; GARANTE QUE A FLAG DE ESTOURO ESTARÁ EM 0.
D_50MS
	MOVLW	.1 ; MOVE 1 PARA O WORK.
	SUBWF	ESTOURO, W ; SUBTRAI 1 DA VARIÁVEL DE ESTOURO ATÉ QUE ELA SEJA ZERO. ISSO NÃO VAI ACONTECER ENQUANTO O PROGRAMA NÃO FOR PARA A INTERRUPÇÃO, ONDE A VARIÁVEL RECEBE O VALOR 1, E POR ISSO, O PROGRAMA FICARÁ NA LABEL ATÉ QUE A CONTAGEM DO TIMER CHEGUE AO VALOR DESEJADO E GERE UMA INTERRUPÇÃO.
	BTFSS	STATUS, Z ; TESTA SE O RESULTADO DEU ZERO.
	GOTO	D_50MS ; PERMANECE NO LOOP ENQUANTO O RESULTADO NÃO DER ZERO.
	RETURN ; APÓS VOLTAR DA INTERRUPÇÃO, O RESULTADO DA SUBTRAÇÃO SERÁ IGUAL A ZERO, E O PROGRAMA RETORNARÁ PARA A LABEL DE ACENDER O DISPLAY, APÓS O DELAY TER OCORRIDO.

